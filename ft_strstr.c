char	*ft_strstr(char *str, char *to_find)
{
	int i;
	int j;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] == to_find[0])
		{
			j = 0;
			while (to_find[j] == str[i+j])
				j++;
			if (to_find[j+1] == '\0')
				return (to_find);
		}
		i++;
	}
	return((void *)0);
}
