void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putnbr(int n)
{
	if (n == -2147483648)
	{
		ft_putstr("â€“2147483648");
		return;
	}
	if (n < 0)
	{
		n *= -1;
		ft_putchar('-');
	}
	if (n / 10)
		ft_putnbr(n / 10);
	ft_putchar((n % 10) + '0');
}
