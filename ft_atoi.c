int	ft_atoi(char *str)
{
	int nb;
	int i;
	int sign;
	int s;

	nb = 0;
	i = 0;
	s = 0;
	while (str[i] <= 32)
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		s = 1;
		sign = (str[i] == '-') ? -1 : 1;
	}
	while (str[i + s] != '\0')
	{
		if (!(str[i + s] <= 57 && str[i + s] >= 48))
			return (0);
		if (i > 0)
			nb *= 10;
		nb += (str[i + s] - 48);
		i++;
	}
	return (nb * sign);
}
